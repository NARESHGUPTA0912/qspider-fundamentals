/**
*This class Program1
*is to demonstarte use of
*Escape characters
*/

public class Program1{

	/**
	*This method main() is
	*designed to execute 
	*different escape characters
	*/
	public static void main(String[] args){

		//This program is to see use of escape characters


		/*Here we will see use of 
		* escape characters such as
		* \", \', \t and so on	
		*/	

		System.out.println("\"Mohan and \"Sohan\" are here\"");
		System.out.println('@');
		System.out.println('@'+0);
		System.out.println('\'');
		System.out.println('\''+0);
		System.out.println("He is \tMo\t\tHan");
		System.out.println("Today is \nThurs\n\nDay");
		System.out.println("Mohan and \\Sohan\\ are here");

		
			
	}
}