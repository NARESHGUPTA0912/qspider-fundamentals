public class Program9 {

    public static void main(int[] args) {
        System.out.println("This is main with int[] args");
    }
    public static void main(String[] args) {
        System.out.println("This is main with String[] args");
    }
    public static void main(int args) {
        System.out.println("This is main with int args");
    }
    public static void main() {
        System.out.println("This is main with no args");
    }
}
